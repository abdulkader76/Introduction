# Functions vs Methods

# e.g of changing string to uppercase
# my_str = 'xyzzy'
# print(my_str.upper())

# my_str.upper() uses method invocation
# prepend the object with a . and the method call - in this case upper()
# these methods are basically functions but in Python they are called methods

# Methods are obj-specific

# example of math module
# import math

# print(math.sqrt(5))

# math.sqrt() is not a method call
# math.sqrt() does not perform operations on the object, its main purpose is to tell Python where to look for those functions

# functions are spoken of when discussing fns and methods
# however, the term methods should be used when talking about functions that designed to be called explicitly on an object