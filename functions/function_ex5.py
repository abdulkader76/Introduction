# 5. What does the following code print?

def scream(words):
    return words + '!!!!'

scream('Yippeee')

# Ans:
# Nothing. The function scream('Yippeee')  returns a value with the return statement but no value will be printed as print function is not called.

# LS solution:
# This program doesn't output anything. The function returns a value, Yipeee!!!!, but it doesn't do anything with it. In particular, it doesn't print it.