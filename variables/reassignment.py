# Reassignment vs Mutation

# num = 3                   # assignment
# my_list = [1, 2, 3]       # assignment
# my_dict = {
#     'a': 1,
#     'b': 2,
# }

# num = 42                  # reassignment
# my_list[1] = 42           # reassignment of                                  element
                            # my_list is mutated
# my_dict['b']              # reassignment of dict                             pair
                            # my_dict is mutated

# You can still re-assign variables
# my_list = (2, 3, 4)       # reassignment
# my_dict = {'x': 0}        # reassignment